using namespace std;
//============================================================
// 最大公因數
// Input: a, b
// Output: GCD(a, b)
//============================================================
int iGCD(int a, int b)
{
int t;
while(b != 0)
	{
	t = b;
	b = a % b;
	a = t;
	}
return a;
}
//============================================================
// 主程式
//============================================================
int main() 
{	
int iGCDTotal;
int i, j;//loop used
int iN;

while(cin >> iN)
	{
	iGCDTotal = 0;
	for(i = 1; i < iN; i++)
		for(j = i + 1;j <= iN; j++)
			iGCDTotal += iGCD(i, j);
	cout << iGCDTotal <<endl;
	}

return 0;
}
