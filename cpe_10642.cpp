using namespace std;
//============================================================
// ？？？
//============================================================
int getF(int x,int y)
{
	int iTemp;
	iTemp = ceil(sqrt(pow(x,2) + pow(y,2)));
	return (1 + iTemp) * iTemp / 2 + iTemp;
}
//============================================================
// 主程式
//============================================================
int main() 
{
	cout << getF(0,2);
	return 0;
}
